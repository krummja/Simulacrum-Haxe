from __future__ import annotations
from dataclasses import dataclass
from enum import Enum
import math


UNIT = 16
WIDTH = 8
HEIGHT = 8


class Space(Enum):
    PIXEL = 0
    WORLD = 1


class Projection:
    @staticmethod
    def pixel_to_world(px: float, py: float) -> Coordinate:
        return Coordinate(px / UNIT, py / UNIT, Space.WORLD)

    @staticmethod
    def world_to_pixel(wx: float, wy: float) -> Coordinate:
        return Coordinate(wx * UNIT, wy * UNIT, Space.PIXEL)


class CoordinateUtils:
    @staticmethod
    def lift(coord: Coordinate, space: Space) -> Coordinate:
        if coord.space != Space.PIXEL:
            coord = Projection.world_to_pixel(coord.x, coord.y)

        match space:
            case Space.PIXEL:
                return coord
            case Space.WORLD:
                return Projection.pixel_to_world(coord.x, coord.y)

    @staticmethod
    def sub(a: Coordinate, b: Coordinate) -> Coordinate:
        projected = CoordinateUtils.lift(b, a.space)
        return Coordinate(a.x - projected.x, a.y - projected.y, a.space)


@dataclass
class Coordinate:
    x: float
    y: float
    space: Space


